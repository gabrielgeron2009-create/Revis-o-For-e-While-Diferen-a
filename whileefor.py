# Usando o laço for
print("Usando for:")
for i in range(1, 11):  # Vai de 1 até 10
    print(i)

# Usando o laço while
print("\nUsando while:")
contador = 1
while contador <= 10:
    print(contador)
    contador += 1

# Usando o laço for
print("Repetições com for:")
for i in range(5):  # Executa 5 vezes (0 a 4)
    print(f"Repetição {i + 1}")

# Usando o laço while
print("\nRepetições com while:")
contador = 0
while contador < 5:
    print(f"Repetição {contador + 1}")
    contador += 1

# Usando o laço for
print("Contando de 2 em 2 com for:")
for i in range(2, 11, 2):  # De 2 até 10, pulando de 2 em 2
    print(i)

# Usando o laço while
print("\nContando de 2 em 2 com while:")
numero = 2
while numero <= 10:
    print(numero)
    numero += 2
